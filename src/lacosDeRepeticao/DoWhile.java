package lacosDeRepeticao;

public class DoWhile {

	public static void main(String[] args) {
		int i = 0;
		do {
			System.out.println(i);
			i++;
		}while (i < 10);{
			System.out.println(i);
		}
		
		System.out.println("\n-----");
		
		int j = 10;
		do {
			System.out.println(j);
			j--;
		}while (j > 0); {
			System.out.println(j);
			
		}
	}

}
