package variaveis;

public class DeclaracaoDeVariaveis {

	public static void main(String[] args) {
		
		int numero1 = 0, numero2 = 1;
		System.out.println("Inteiro: " + (numero1 + numero2));
		
		double num1 = 0.5, num2 = 1.5;
		System.out.println("Double: " + (num1 + num2));
		
		float numFlutuante = 0.5f;
		System.out.println("Float: " + numFlutuante);
		
		char letra = 'B';
		System.out.println("Char: " + letra);
		
		boolean amarelo = false;
		System.out.println(amarelo);
		
		String palavra = "Cadeira";
		System.out.println("String: " + palavra);
		
	}

}
